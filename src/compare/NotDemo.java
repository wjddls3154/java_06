package compare;

public class NotDemo {
    public static void main(String[] args) {

        System.out.println(1!=2); // 값 : true
        System.out.println(1 != 1); // 값 : false
        System.out.println("one"!="two"); // 값 : true
        System.out.println("one" != "one"); // 값 : false

        /**
         * not 연산자 : !=
         * 같지 않다는 뜻이기에, 같지 않으면 true 같으면 false 결과값이 나온다.
         */

    }
}
